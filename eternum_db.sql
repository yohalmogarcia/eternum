-- Función para generar UUID en mayúsculas
CREATE OR REPLACE FUNCTION public.generate_uppercase_uuid()
RETURNS varchar(36) AS $$
BEGIN
    RETURN UPPER(gen_random_uuid()::varchar(36));
END;
$$ LANGUAGE plpgsql;

-- Función para formatear número de secuencia de 15 dígitos
CREATE OR REPLACE FUNCTION public.formatear_numero_secuencia(p_numero integer)
RETURNS varchar(15) AS $$
BEGIN
    RETURN LPAD(p_numero::varchar, 15, '0');
END;
$$ LANGUAGE plpgsql;

-- Función para actualizar updated_date automáticamente
CREATE OR REPLACE FUNCTION public.update_updated_date_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_date = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ============================================================
-- TRIGGERS para actualizar updated_date automáticamente
-- ============================================================
CREATE TRIGGER trg_users_updated_date BEFORE UPDATE ON public.users
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_date_column();

