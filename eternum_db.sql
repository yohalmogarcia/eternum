    photo_url               varchar(500) NOT NULL DEFAULT '',
    caption                 varchar(255) NOT NULL DEFAULT '',
    display_order           integer NOT NULL DEFAULT 0,
    is_main_photo           boolean NOT NULL DEFAULT false,
    created_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_by              integer NOT NULL DEFAULT 0,
    updated_by              integer NOT NULL DEFAULT 0,
    CONSTRAINT fk_memorial_photo_memorial FOREIGN KEY (fk_memorial)
        REFERENCES public.memorial(pk_memorial) ON DELETE CASCADE
);

COMMENT ON TABLE public.memorial_photo IS 'Fotos y multimedia asociadas a cada memorial';

-- Índices para memorial_photo
CREATE INDEX idx_photo_memorial ON public.memorial_photo(fk_memorial);
CREATE INDEX idx_photo_main ON public.memorial_photo(is_main_photo) WHERE is_main_photo = true;

-- ============================================================
-- 4. TABLA: deactivation_request (Solicitudes de desactivación)
-- ============================================================
CREATE TABLE public.deactivation_request (
