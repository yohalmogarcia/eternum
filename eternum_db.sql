-- Crear base de datos
CREATE DATABASE eternum_db
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'en_US.utf8'
    LC_CTYPE = 'en_US.utf8'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

-- Conectar a la base de datos
\c eternum_db;

-- Habilitar extensión pgcrypto para encriptación
CREATE EXTENSION IF NOT EXISTS "pgcrypto" SCHEMA public;

-- ============================================================
-- 1. TABLA: users (Usuarios del sistema)
-- ============================================================
CREATE TABLE public.users (
    pk_users                integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email                   varchar(255) NOT NULL DEFAULT '',
    password_hash           varchar(255) NOT NULL DEFAULT '',
    first_name              varchar(100) NOT NULL DEFAULT '',
    last_name               varchar(100) NOT NULL DEFAULT '',
    phone                   varchar(20) NOT NULL DEFAULT '',
    is_email_verified       boolean NOT NULL DEFAULT false,
    email_verification_token varchar(255) NOT NULL DEFAULT '',
    password_reset_token    varchar(255) NOT NULL DEFAULT '',
    password_reset_expires  timestamp without time zone,
    is_active               boolean NOT NULL DEFAULT true,
    has_consent_privacy     boolean NOT NULL DEFAULT false,
    consent_date            timestamp without time zone,
    created_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_by              integer NOT NULL DEFAULT 0,
    updated_by              integer NOT NULL DEFAULT 0,
    CONSTRAINT uk_users_email UNIQUE (email)
);

COMMENT ON TABLE public.users IS 'Usuarios registrados en la plataforma Eternum';

-- Índices para users
CREATE INDEX idx_users_email ON public.users(email);
CREATE INDEX idx_users_active ON public.users(is_active);

-- ============================================================
-- 2. TABLA: memorial (Memoriales de fallecidos)
-- ============================================================
CREATE TABLE public.memorial (
    pk_memorial             integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fk_user                 integer NOT NULL,
    deceased_name           varchar(200) NOT NULL DEFAULT '',
    deceased_last_name      varchar(200) NOT NULL DEFAULT '',
    birth_date              date,
    death_date              date NOT NULL,
    biography               text NOT NULL DEFAULT '',
    epitaph                 varchar(500) NOT NULL DEFAULT '',
    profile_photo_url       varchar(500) NOT NULL DEFAULT '',
    is_featured             boolean NOT NULL DEFAULT false,
    candle_count            integer NOT NULL DEFAULT 0,
    is_active               boolean NOT NULL DEFAULT true,
    created_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_date            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_by              integer NOT NULL DEFAULT 0,
    updated_by              integer NOT NULL DEFAULT 0,
    CONSTRAINT fk_memorial_user FOREIGN KEY (fk_user)
        REFERENCES public.users(pk_users) ON DELETE CASCADE
);

COMMENT ON TABLE public.memorial IS 'Perfiles de memoriales para recordar a seres queridos fallecidos';

-- Índices para memorial
CREATE INDEX idx_memorial_user ON public.memorial(fk_user);
CREATE INDEX idx_memorial_dates ON public.memorial(death_date, birth_date);
CREATE INDEX idx_memorial_featured ON public.memorial(is_featured) WHERE is_featured = true;
CREATE INDEX idx_memorial_active ON public.memorial(is_active);

-- ============================================================
-- 3. TABLA: memorial_photo (Galería de fotos por memorial)
-- ============================================================
CREATE TABLE public.memorial_photo (
