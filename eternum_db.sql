COMMENT ON TABLE public.deactivation_request IS 'Solicitudes para desactivar cuentas de redes sociales de fallecidos';

-- Índices para deactivation_request
CREATE INDEX idx_deactivation_requester ON public.deactivation_request(fk_requester_user);
CREATE INDEX idx_deactivation_status ON public.deactivation_request(request_status);

-- ============================================================
-- 5. TABLA: subscription (Suscripciones de usuarios)
